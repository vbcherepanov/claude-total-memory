# Persistence
# ────────────────────────────────────────────────────────────────────


def test_save_load_roundtrip(tmp_path: Path) -> None:
    """Save and reload preserves parameters bit-exact (via JSON repr)."""
    cal = PlattCalibrator(a=-2.71828, b=1.41421)
    out = tmp_path / "cal.json"
    save(cal, out)
    cal2 = load(out)
    assert cal2.a == pytest.approx(cal.a, rel=1e-12)
    assert cal2.b == pytest.approx(cal.b, rel=1e-12)


def test_load_rejects_wrong_kind(tmp_path: Path) -> None:
    """Loading a JSON with kind != 'platt' raises ValueError."""
    p = tmp_path / "bad.json"
    p.write_text(json.dumps({"kind": "isotonic", "a": 0.0, "b": 0.0}))
    with pytest.raises(ValueError):
        load(p)


def test_load_rejects_non_finite(tmp_path: Path) -> None:
    """Loading a JSON with non-finite params raises ValueError.

    JSON does not natively support NaN; we write a bare string and
    check that ``float()`` failure surfaces a ValueError.
    """
    p = tmp_path / "nan.json"
    p.write_text(json.dumps({"kind": "platt", "a": "not a number", "b": 0.0}))
    with pytest.raises(ValueError):
        load(p)


def test_save_creates_parent_directories(tmp_path: Path) -> None:
    """Save creates intermediate directories on demand."""
    cal = PlattCalibrator(a=-1.0, b=0.5)
    out = tmp_path / "deep" / "nested" / "cal.json"
    save(cal, out)
    assert out.exists()
    cal2 = load(out)
    assert cal2.a == pytest.approx(-1.0)
    assert cal2.b == pytest.approx(0.5)
